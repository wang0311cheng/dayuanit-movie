package com.dayuanit.movie.dto;


public class FilmDTO {

    private String filmName;

    private String filmCode;

    private String filmPic;

    private String filmType;

    private String filmTime;

    private String filmShowTime;

    private String plot;

    public String getFilmName() {
        return filmName;
    }

    public void setFilmName(String filmName) {
        this.filmName = filmName;
    }

    public String getFilmCode() {
        return filmCode;
    }

    public void setFilmCode(String filmCode) {
        this.filmCode = filmCode;
    }

    public String getFilmPic() {
        return filmPic;
    }

    public void setFilmPic(String filmPic) {
        this.filmPic = filmPic;
    }

    public String getFilmType() {
        return filmType;
    }

    public void setFilmType(String filmType) {
        this.filmType = filmType;
    }

    public String getFilmTime() {
        return filmTime;
    }

    public void setFilmTime(String filmTime) {
        this.filmTime = filmTime;
    }

    public String getFilmShowTime() {
        return filmShowTime;
    }

    public void setFilmShowTime(String filmShowTime) {
        this.filmShowTime = filmShowTime;
    }

    public String getPlot() {
        return plot;
    }

    public void setPlot(String plot) {
        this.plot = plot;
    }
}
